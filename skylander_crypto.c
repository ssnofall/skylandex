static const uint8_t RCON[10] = {
    0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36,
};

static uint8_t gf_mul(uint8_t a, uint8_t b) {
    uint8_t p = 0;
    for(int i = 0; i < 8; i++) {
        if(b & 1) p ^= a;
        uint8_t hi = a & 0x80;
        a <<= 1;
        if(hi) a ^= 0x1B;
        b >>= 1;
    }
    return p;
}

static void aes128_expand_key(const uint8_t key[16], uint8_t round_keys[176]) {
    memcpy(round_keys, key, 16);
    for(uint8_t i = 1; i < 11; i++) {
        uint8_t* prev = round_keys + (i - 1) * 16;
        uint8_t* curr = round_keys + i * 16;
        curr[0] = SBOX[prev[13]] ^ RCON[i - 1] ^ prev[0];
        curr[1] = SBOX[prev[14]] ^ prev[1];
        curr[2] = SBOX[prev[15]] ^ prev[2];
        curr[3] = SBOX[prev[12]] ^ prev[3];
        for(uint8_t j = 4; j < 16; j++) {
            curr[j] = curr[j - 4] ^ prev[j];
        }
    }
}

static void aes128_decrypt_block(const uint8_t round_keys[176], const uint8_t in[16], uint8_t out[16]) {
    uint8_t state[16];
    memcpy(state, in, 16);

    /* AddRoundKey (round 10) */
    for(uint8_t i = 0; i < 16; i++) state[i] ^= round_keys[160 + i];

    for(int8_t round = 9; round >= 1; round--) {
        /* InvShiftRows */
        uint8_t t;
        t = state[13]; state[13] = state[9]; state[9] = state[5]; state[5] = state[1]; state[1] = t;
        t = state[2]; state[2] = state[10]; state[10] = t;
        t = state[6]; state[6] = state[14]; state[14] = t;
        t = state[3]; state[3] = state[7]; state[7] = state[11]; state[11] = state[15]; state[15] = t;

        /* InvSubBytes */
        for(uint8_t i = 0; i < 16; i++) state[i] = INV_SBOX[state[i]];

        /* AddRoundKey */
        for(uint8_t i = 0; i < 16; i++) state[i] ^= round_keys[round * 16 + i];

        /* InvMixColumns */
        for(uint8_t c = 0; c < 16; c += 4) {
            uint8_t s0 = state[c], s1 = state[c + 1], s2 = state[c + 2], s3 = state[c + 3];
            state[c]     = gf_mul(0x0E, s0) ^ gf_mul(0x0B, s1) ^ gf_mul(0x0D, s2) ^ gf_mul(0x09, s3);
            state[c + 1] = gf_mul(0x09, s0) ^ gf_mul(0x0E, s1) ^ gf_mul(0x0B, s2) ^ gf_mul(0x0D, s3);
            state[c + 2] = gf_mul(0x0D, s0) ^ gf_mul(0x09, s1) ^ gf_mul(0x0E, s2) ^ gf_mul(0x0B, s3);
            state[c + 3] = gf_mul(0x0B, s0) ^ gf_mul(0x0D, s1) ^ gf_mul(0x09, s2) ^ gf_mul(0x0E, s3);
        }
    }

    /* InvShiftRows (round 0) */
    uint8_t t;
    t = state[13]; state[13] = state[9]; state[9] = state[5]; state[5] = state[1]; state[1] = t;
    t = state[2]; state[2] = state[10]; state[10] = t;
    t = state[6]; state[6] = state[14]; state[14] = t;
    t = state[3]; state[3] = state[7]; state[7] = state[11]; state[11] = state[15]; state[15] = t;

    /* InvSubBytes (round 0) */
    for(uint8_t i = 0; i < 16; i++) state[i] = INV_SBOX[state[i]];

    /* AddRoundKey (round 0) */
    for(uint8_t i = 0; i < 16; i++) state[i] ^= round_keys[i];

    memcpy(out, state, 16);
}

