static inline uint32_t rotl32(uint32_t x, int n) {
    return (x << n) | (x >> (32 - n));
}

static void md5_process_block(uint32_t state[4], const uint8_t block[64]) {
    uint32_t M[16];
    for(int i = 0; i < 16; i++) {
        M[i] = (uint32_t)block[i * 4] | ((uint32_t)block[i * 4 + 1] << 8) |
               ((uint32_t)block[i * 4 + 2] << 16) | ((uint32_t)block[i * 4 + 3] << 24);
    }

    uint32_t A = state[0], B = state[1], C = state[2], D = state[3];

    for(int i = 0; i < 64; i++) {
        uint32_t F;
        if(i < 16) {
            F = (B & C) | (~B & D);
        } else if(i < 32) {
            F = (D & B) | (~D & C);
        } else if(i < 48) {
            F = B ^ C ^ D;
        } else {
            F = C ^ (B | ~D);
        }
        F += A + MD5_T[i] + M[MD5_G[i]];
        A = D;
        D = C;
        C = B;
        B += rotl32(F, MD5_S[i]);
    }

    state[0] += A;
    state[1] += B;
    state[2] += C;
    state[3] += D;
}

static void md5_hash(const uint8_t* input, size_t ilen, uint8_t output[16]) {
    uint32_t state[4] = {0x67452301, 0xefcdab89, 0x98badcfe, 0x10325476};

    size_t full_blocks = ilen / 64;
    for(size_t i = 0; i < full_blocks; i++) {
        md5_process_block(state, input + i * 64);
    }

    size_t remaining = ilen % 64;
    uint8_t buf[128];
    memset(buf, 0, sizeof(buf));
    if(remaining > 0) memcpy(buf, input + full_blocks * 64, remaining);
    buf[remaining] = 0x80;

    size_t pad_len;
    if(remaining < 56) {
        pad_len = 64;
    } else {
        pad_len = 128;
    }

    uint64_t bit_len = (uint64_t)ilen * 8;
    buf[pad_len - 8] = (uint8_t)(bit_len);
    buf[pad_len - 7] = (uint8_t)(bit_len >> 8);
    buf[pad_len - 6] = (uint8_t)(bit_len >> 16);
    buf[pad_len - 5] = (uint8_t)(bit_len >> 24);
    buf[pad_len - 4] = (uint8_t)(bit_len >> 32);
    buf[pad_len - 3] = (uint8_t)(bit_len >> 40);
    buf[pad_len - 2] = (uint8_t)(bit_len >> 48);
    buf[pad_len - 1] = (uint8_t)(bit_len >> 56);

    for(size_t i = 0; i < pad_len; i += 64) {
        md5_process_block(state, buf + i);
    }

    for(int i = 0; i < 4; i++) {
        output[i * 4]     = (uint8_t)(state[i]);
        output[i * 4 + 1] = (uint8_t)(state[i] >> 8);
        output[i * 4 + 2] = (uint8_t)(state[i] >> 16);
        output[i * 4 + 3] = (uint8_t)(state[i] >> 24);
    }
}

/* --- Skylander AES-128 application-layer decryption --- */

static void derive_aes_key(const MfClassicBlock blocks[2], uint8_t block_index, uint8_t key_out[16]) {
    uint8_t key_input[86];
    memcpy(key_input, blocks[0].data, 16);
    memcpy(key_input + 16, blocks[1].data, 16);
    key_input[32] = block_index;
    memcpy(key_input + 33, ACTIVISION_CONSTANT, 53);
    md5_hash(key_input, 86, key_out);
}

static void decrypt_block(const uint8_t aes_key[16], const uint8_t in[16], uint8_t out[16]) {
    uint8_t round_keys[176];
    aes128_expand_key(aes_key, round_keys);
    aes128_decrypt_block(round_keys, in, out);
}

void skylander_crypto_decrypt_blocks(
    const MfClassicBlock blocks[SKYLANDER_TOTAL_BLOCKS],
    MfClassicBlock out[SKYLANDER_TOTAL_BLOCKS]) {

    memcpy(out, blocks, sizeof(MfClassicBlock) * SKYLANDER_TOTAL_BLOCKS);

    static const uint8_t ENCRYPTED_BLOCKS[] = {
        0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F,
        0x24, 0x25, 0x26, 0x27, 0x28, 0x29, 0x2A, 0x2B,
        0x2C, 0x2D, 0x2E, 0x2F,
    };

    for(size_t i = 0; i < sizeof(ENCRYPTED_BLOCKS); i++) {
        uint8_t block_index = ENCRYPTED_BLOCKS[i];
        uint8_t aes_key[16];
        derive_aes_key(blocks, block_index, aes_key);

        uint8_t decrypted[16];
        decrypt_block(aes_key, blocks[block_index].data, decrypted);
        memcpy(out[block_index].data, decrypted, 16);
    }

    FURI_LOG_D(TAG, "Decrypted %zu application blocks", sizeof(ENCRYPTED_BLOCKS));
}
